numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

print(f"Os números digitados são iguais: {numero1 == numero2}")
print(f"Os números digitados são diferentes: {numero1 != numero2}")
print(f"O primeiro número digitado é maior que o primeiro: {numero1 > numero2}")
print(f"O primeiro número digitado é menor que o primeiro: {numero1 < numero2}")
print(f"O primeiro número digitado é maior ou igual que o primeiro: {numero1 >= numero2}")
print(f"O primeiro número digitado é menor ou igual que o primeiro: {numero1 <= numero2}")